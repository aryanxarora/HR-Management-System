    private void addTeacherSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTeacherSubmitActionPerformed
        try {
            boolean exists = false;
            String name = teacherName.getText();
            int age = Integer.parseInt(teacherAge.getText());
            String gender = "";
            if(teacherMale.isSelected()){
                gender = "Male";
            } else if(teacherFemale.isSelected()){
                gender = "Female";
            } else {
                JOptionPane.showMessageDialog(null, "No Gender Selected!");
            }
            String address = teacherAddress.getText();
            String phone = teacherPhone.getText();
            int id = Integer.parseInt(teacherID.getText());
            String specialty = teacherSpecialty.getText();
            int degree = teacherDegree.getSelectedIndex() + 1;
            boolean fullTime = false;
            if(teacherFullTime.isSelected()){
                fullTime = true;
            } else if(teacherPartTime.isSelected()){
                fullTime = false;
            } else {
                JOptionPane.showMessageDialog(null, "No Employment Type Selected!");
            }
            int hoursWorked = 32;
            if(!fullTime){
                hoursWorked = Integer.parseInt(teacherHoursWorked.getText());
            }
            int department = teacherDepartment.getSelectedIndex();
            Teacher teacher = new Teacher(name,age, gender, address, phone, id, specialty, degree, false, fullTime, hoursWorked);
            
            for(int i = 0; i < departments.get(department).getTeachers().size(); i++){
                if(departments.get(department).getTeachers().get(i).equals(teacher)){
                    JOptionPane.showMessageDialog(null, name);
                    exists = true;
                }
            }
            
            if(!exists){
                departments.get(department).addTeacher(teacher);
                computerScienceDepartment.setText(departments.get(0).display());
                liberalArtsDepartment.setText(departments.get(1).display());
                businessDepartment.setText(departments.get(2).display());
                driver.saveProgress(departments);
                addTeacherClear.doClick();
                JOptionPane.showMessageDialog(null, "Teacher Added!");
            }
            
        } catch (Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Invalid Submission!");
        }
    }//GEN-LAST:event_addTeacherSubmitActionPerformed

