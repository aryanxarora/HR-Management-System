    private void staffSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_staffSubmitActionPerformed
        // TODO add your handling code here:
        try {
            boolean exists = false;
            String name = staffName.getText();
            int age = Integer.parseInt(staffAge.getText());
            String gender = "";
            if(staffMale.isSelected()){
                gender = "Male";
            } else if(staffFemale.isSelected()){
                gender = "Female";
            } else {
                // THROW EXCEPTION
                JOptionPane.showMessageDialog(null, "No Gender Selected!");
            }
            String address = staffAddress.getText();
            String phone = staffPhone.getText();
            String duty = staffDuty.getText();
            int workload = Integer.parseInt(staffWorkload.getText());
            if(workload > 40){
                JOptionPane.showMessageDialog(null, "Staff Workload Cannot Exceed 40!");
                exists = true;
            }
            int department = staffDepartment.getSelectedIndex();
            Staff staff = new Staff(name,age, gender, address, phone, duty, workload);
            
            for(int i = 0; i < departments.get(department).getStaff().size(); i++){
                if(departments.get(department).getStaff().get(i).equals(staff)){
                    JOptionPane.showMessageDialog(null, "Staff Already Exists!");
                    exists = true;
                }
            }
            
            if(!exists){
                departments.get(department).addStaff(staff);
                computerScienceDepartment.setText(departments.get(0).display());
                liberalArtsDepartment.setText(departments.get(1).display());
                businessDepartment.setText(departments.get(2).display());
                driver.saveProgress(departments);
                staffClear.doClick();
                JOptionPane.showMessageDialog(null, "Staff Added!");
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Invalid Submission!");
        }
    }//GEN-LAST:event_staffSubmitActionPerformed

